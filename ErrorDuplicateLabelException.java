public class ErrorDuplicateLabelException extends Exception {
    public ErrorDuplicateLabelException() { super(); }
    public ErrorDuplicateLabelException(String message) { super(message); }
    public ErrorDuplicateLabelException(String message, Throwable cause) { super(message, cause); }
    public ErrorDuplicateLabelException(Throwable cause) { super(cause); }
}